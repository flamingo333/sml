package org.hw.sml.core.resolver.exception;

public class TagException extends ResolverException{
	/**
	 * 
	 */
	private static final long serialVersionUID = -4384694426603697521L;

	public TagException() {
		super();
	}

	public TagException(String message, Throwable cause) {
		super(message, cause);
	}

	public TagException(String msg) {
		super(msg);
	}

	public TagException(Throwable cause) {
		super(cause);
	}
	
}
